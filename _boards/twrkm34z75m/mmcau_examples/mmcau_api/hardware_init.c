/*${function:start}*/
void BOARD_InitHardware(void)
{
    BOARD_InitPins();
    BOARD_BootClockRUN();
    BOARD_InitDebugConsole();

    /* Enable MMCAU clock gate in SIM */
    SIM->SCGC7 |= SIM_SCGC7_CAU(1u);
}
/*${function:end}*/
